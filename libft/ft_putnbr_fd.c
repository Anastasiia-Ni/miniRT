#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int		nb;
	char	c;

	nb = n;
	if (nb == -2147483648)
	{
		write(fd, "-214748364", 10);
		nb = 8;
	}
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = (nb) * -1;
	}
	if (nb >= 10)
		ft_putnbr_fd(nb / 10, fd);
	c = (nb % 10) + '0';
	write(fd, &c, 1);
}
